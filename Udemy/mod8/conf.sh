#!/usr/bin/env bash

# ------------------------------- VARIÁVEIS ----------------------------------------- #
CONFIGURACAO="archive.conf"

USAR_CORES=
USAR_MAIUSCULAS=

MSG="Olá Mundo!"

VERDE="\033[32m"
VERMELHO="\033[31m"
# ------------------------------------------------------------------------ #

# ------------------------------- TESTES ----------------------------------------- #
[ ! -r "$CONFIGURACAO" ] && echo -e "${VERMELHO}ERRO DE LEITURA" && exit 1
# ------------------------------------------------------------------------ #

# ------------------------------- FUNÇÕES ----------------------------------------- #

# ------------------------------------------------------------------------ #

# ------------------------------- EXECUÇÃO ----------------------------------------- #
while read -r lines; do
    [ "$(echo $lines | cut -c1)" = '#' ] && continue
    [ ! $lines ] && continue
    echo "$lines"
done < $CONFIGURACAO

